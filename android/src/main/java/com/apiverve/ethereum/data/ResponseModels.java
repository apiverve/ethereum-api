package com.apiverve.ethereum.data;

import com.fasterxml.jackson.annotation.*;
import java.time.OffsetDateTime;

public class EthereumPriceData {
    private String currency;
    private double price;
    private long marketCap;
    private long volume24H;
    private double change24H;
    private OffsetDateTime lastUpdated;

    @JsonProperty("currency")
    public String getCurrency() { return currency; }
    @JsonProperty("currency")
    public void setCurrency(String value) { this.currency = value; }

    @JsonProperty("price")
    public double getPrice() { return price; }
    @JsonProperty("price")
    public void setPrice(double value) { this.price = value; }

    @JsonProperty("marketCap")
    public long getMarketCap() { return marketCap; }
    @JsonProperty("marketCap")
    public void setMarketCap(long value) { this.marketCap = value; }

    @JsonProperty("volume24h")
    public long getVolume24H() { return volume24H; }
    @JsonProperty("volume24h")
    public void setVolume24H(long value) { this.volume24H = value; }

    @JsonProperty("change24h")
    public double getChange24H() { return change24H; }
    @JsonProperty("change24h")
    public void setChange24H(double value) { this.change24H = value; }

    @JsonProperty("lastUpdated")
    public OffsetDateTime getLastUpdated() { return lastUpdated; }
    @JsonProperty("lastUpdated")
    public void setLastUpdated(OffsetDateTime value) { this.lastUpdated = value; }
}